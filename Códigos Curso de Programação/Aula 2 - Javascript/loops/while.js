let count = 0;

/* 
  O laço de repetição do While é formado por uma condição.
  Enquanto ela for verdadeira, o código dentro do loop será executado.
  Você também pode adicionar um loop dentro de outro loop!
*/
while (count <= 5) {
  console.log("Contador: " + count);
  count += 1;
}
