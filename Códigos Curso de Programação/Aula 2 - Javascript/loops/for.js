for (let i = 0; i <= 5; i++) {
  console.log("Contador: " + i);
}

/*
  Diferente do while, o for faz o loop acontecer apartir
  de uma variável criada na declaração dele, no caso do nosso
  exemplo, essa veriável é o "i". Novamente, enquanto a variável
  se encaixar na condição passada ao for, o trecho de código será
  executado. Nesse caso, o nosso contador é somado automaticamente,
  diferente do exemplo do while.
*/
